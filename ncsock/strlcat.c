#include "include/debianfix.h"
#include <string.h>

u64 strlcat(char* dst, const char* src, u64 siz)
{
  register char *d = dst;
  register const char *s = src;
  register u64 n = siz;
  u64 dlen;

  while (n-- != 0 && *d != '\0')
    d++;
  dlen = d - dst;
  n = siz - dlen;

  if (n == 0)
    return(dlen + strlen(s));
  while (*s != '\0') {
    if (n != 1) {
      *d++ = *s;
      n--;
    }
    s++;
  }
  *d = '\0';

  return(dlen + (s - src));
}
